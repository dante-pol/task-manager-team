def plus(a: int,b: int) ->int:
    """
    :param a: целое число
    :param b: целое число
    :return: сумма параметров
    """
    return a + b

def minus(a: int ,b: int) -> int:
    """
    :param a: целое число
    :param b: целое число
    :return: разность параметров
    """
    return a - b

def mult(a: int ,b: int) -> int:
    """
    :param a: целое число
    :param b: целое число
    :return: произведение параметров
    """
    return a * b
